def get_diff(metric1, metric2):
    r2 = round(metric1[0] - metric2[0], 2)
    b10 = round(metric1[1] - metric2[1], 2)
    wmspe = round(metric2[2] - metric1[2], 2)
    diff = []
    for metric in [r2, b10, wmspe]:
        diff.append(f"+{metric}" if metric >= 0 else f"{metric}")

    return diff

